<?php

/**
 * Return sum of $a and $b
 * @param int $a
 * @param int $b
 * @return int Sum of $a and $b
 */
function get_sum(int $a, int $b): int
{
    return $a + $b;
}

/**
 * Return multiplication of $a and $b
 * @param int $a
 * @param int $b
 * @return int Multiplication of $a and $b
 */
function multiplication(int $a, int $b): int
{
    return $a * $b;
}